#include <iostream>
using namespace std;
// Для данного вещественного числа х найти функцию, принимающей значение целого типа: 
// 0, если х<0, 
// 1, если х принадлежит [0,1), [2,3), 
// -1, если [1,2), [3,4)

int main()
{
    double x;
    cout << "x = ";
    cin >> x;
    double f;

    if (x < 0) {
        f = 0;
    }
    if ((x >= 0 && x < 1) || (x >= 2 && x < 3)) {
        f = 1;
    }
    if ((x >= 1 && x < 2) || (x >= 3 && x < 4)) {
        f = -1;
    }
    cout << "f = " << f;
}

